public class loops {
    public static void main(String[] args) {

        // For loop
        for (int i=0;i<10;i=i+2){
            System.out.println(i*i);
        }

        // Java while loop
        int key=10;
        while (key>5) {
            System.out.println(key);
            key--;
        }
    }
}
