#include <iostream>
using namespace std;
int main(){
    int x,y;
    cout<<"Enter the value of x\n";
    cin>>x;
    cout<<"Enter the value of y\n";
    cin>>y;

    cout<<"Sum is "<<x+y<<"\n";
    cout<<"Difference is "<<x-y<<endl;
    cout<<"Product is "<<x*y<<endl;
    cout<<"Quotient is "<<x/y<<endl;
    cout<<"Remainder is "<<x%y<<endl;
    return 0;
}